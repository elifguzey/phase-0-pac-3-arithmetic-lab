function add(x , y) {
return x+y ;    
}
function subtract (x,y) {
return x-y ;
}
function multiply (x,y) {
    return x*y ;
}
function divide (x,y) {
    return x/y ;
}
function increment (x) {
    return ++ x
}
function decrement (x) {
    return -- x
}
function makeInt (n) {
    return parseInt(n,10)
}
function preserveDecimal (n) {
    return parseFloat (n,10)
} 